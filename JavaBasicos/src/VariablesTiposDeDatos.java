
public class VariablesTiposDeDatos {

	public static void main(String[] args) {
		
		//String permite almacenar cadenas de texto
		String nombre = "Alexis Glez";
		//int edad = 25;
		
		// char nos permite almacenar un caracter, pero sólo 1
		char inicial = 'A';
		
		//Boolean almacena un valor booleano, puede ser true o false
		boolean tienesCrush = true;
		
		// tipos de datos enteros
		
		//byte -128 a 127
		byte edad= 25;
		
		
		//short  -32768 a 32767
		short suma = 24 + 104;
		
		//int
		int numero = 1021321312;
		
		//long
		long numeroLargo = 100_000_000;
		
		System.out.println(numeroLargo);
		
		//float, si tiene decimal lo almacena como double
		float promedio = 1232.23232f;
		
		//double 
		double numeroPi = 3.1415;;
		
		
		/*
		Javascript
		let numero = 10;
		let nombre = 'Alexis Glez'
		*/
	}
}
